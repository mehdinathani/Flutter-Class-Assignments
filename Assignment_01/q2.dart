void main() {
  // Define two variables to store ages
  int age1 = 10;
  int age2 = 20;


  // Check if age1 is greater than age2
  if (age1 > age2) {
    // If age1 is greater than age2, then age1 is the oldest
    print("Person 1 is the oldest");
  } else {
    // If age1 is not greater than age2, then age2 is the oldest
    print("Person 2 is the oldest");
  }
}


