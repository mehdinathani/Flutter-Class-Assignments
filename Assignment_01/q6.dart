void main() {
  // Define a variable to store the alphabet
  String alphabet = 'a';

  // Check if the alphabet is a vowel
  if (alphabet == 'a' || alphabet == 'e' || alphabet == 'i' || alphabet == 'o' || alphabet == 'u') {
    print('The alphabet is a vowel.');
  } else {
    print('The alphabet is a consonant.');
  }
}
