void main() {
  // Create two integer variables
  int length = 10;
  int breadth = 10;

  // Check if both values are equal
  if (length == breadth) {
    // If both values are equal, then it's a square
    print("It's a square");
  } else {
    // If both values are not equal, then it's a rectangle
    print("It's a rectangle");
  }
}
